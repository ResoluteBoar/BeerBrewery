package classes;

public class Beer {
    private String name;
    private String type;
    private Float alcoStrength;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Float getAlcoStrength() {
        return alcoStrength;
    }

    public void setAlcoStrength(Float alcoStrength) {
        this.alcoStrength = alcoStrength;
    }

}